package semantico;

/**
 *
 * @author Aloisio
 */
public class Constante {
    
    private String nome;
    private String tipo;
    private String valor;
    private boolean isCorrectValue;

    /**
     * @return the nome
     */
    public String getNome() {
        return nome;
    }

    /**
     * @param nome the nome to set
     */
    public void setNome(String nome) {
        this.nome = nome;
    }

    /**
     * @return the tipo
     */
    public String getTipo() {
        return tipo;
    }

    /**
     * @param tipo the tipo to set
     */
    public void setTipo(String tipo) {
        this.tipo = tipo;
    }

    /**
     * @return the valor
     */
    public String getValor() {
        return valor;
    }

    /**
     * @param valor the valor to set
     */
    public void setValor(String valor) {
        this.valor = valor;
    }

    /**
     * @return the isCorrectValue
     */
    public boolean isIsCorrectValue() {
        return isCorrectValue;
    }

    /**
     * @param isCorrectValue the isCorrectValue to set
     */
    public void setIsCorrectValue(boolean isCorrectValue) {
        this.isCorrectValue = isCorrectValue;
    }
    
}
